<h3>Where to start:</h3>
<div class="pane">
	<ol>
		<li>All galleries must be in a category.</li>
		<li><a href="<?php echo get_bloginfo('wpurl'); ?>/wp-admin/index.php?page=main_galleries_add_admin&seo_menu_action=create-new">Add a new category</a> (or just use the existing 'Uncategorized')</li>
		<li><a href="<?php echo get_bloginfo('wpurl'); ?>/wp-admin/index.php?page=main_galleries_add_admin&seo_menu_action=create-new">Add galleries</a> to a category</li>
		<li>Go to the <a href="<?php echo get_bloginfo('wpurl'); ?>/wp-admin/index.php?page=main_galleries_add_admin&seo_menu_action=galleries-manage">Manage Galleries</a> page and click 'Edit' to add images into a gallery.</li>
		<li>On the <a href="<?php echo get_bloginfo('wpurl'); ?>/wp-admin/index.php?page=main_galleries_add_admin&seo_menu_action=galleries-manage">Manage Galleries</a> page you will find the 'Short Code' for that galllery Ex: [seo-gallery category="Featured" gallery="Gallery 1"]</li>
		<li>Copy and paste that code into the text editor in a page or post.</li>
		<li>For detailed explanation, see the <a href="<?php echo get_bloginfo('wpurl'); ?>/wp-admin/index.php?page=main_galleries_add_admin&seo_menu_action=display-instructions">Display Instructions</a> page.</li>
	</ol>
